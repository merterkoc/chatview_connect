  @override
  Future<bool> fetchAndUpdateLastMessage({required String chatId}) async {
    try {
      final messageCollectionRef = _messageCollectionRef(chatId)
          .orderBy(
            MessageSortBy.dateTime.key,
            descending: MessageSortOrder.desc.isDesc,
